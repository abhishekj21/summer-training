public class Prog2 {
  public static void main(String[] args) {
    int a = 10;
    int b = 20;
    System.out.println(a + b); // Outputs 30
    System.out.println(a - b); // Outputs -10
    System.out.println(a * b); // Outputs 200  
    System.out.println(a / b); // Outputs 0
    System.out.println(a % b); // Outputs 10 
  }
}
